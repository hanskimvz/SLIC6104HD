#ifndef _IPSDK_VERSION_H_
#define _IPSDK_VERSION_H_

/**
 * IPSDK Version Defination.
 */
/**
 * Major Version Number.
 */
#if !defined(IPSDK_VER_MAJ)
#define IPSDK_VER_MAJ                (1U)
#endif

/**
 * Minor Version Number.
 */
#if !defined(IPSDK_VER_MIN)
#define IPSDK_VER_MIN                (1U)
#endif

/**
 * Revision Version Number.
 */
#if !defined(IPSDK_VER_REV)
#define IPSDK_VER_REV                (1U)
#endif

/**
 * Version Number Code.
 */
#if !defined(IPSDK_VER_NUM)
#define IPSDK_VER_NUM                (2U)
#endif

/**
 * Version Text
 */
#if !defined(IPSDK_VER_STR)
#define IPSDK_VER_STR()                "1.1.1.2"
#endif

/**
 * Version Release Year Value.
 */
#if !defined(IPSDK_VER_DATE_YEAR)
#define IPSDK_VER_DATE_YEAR           (2023U)
#endif

/**
 * Version Release Month Value.
 */
#if !defined(IPSDK_VER_DATE_MON)
#define IPSDK_VER_DATE_MON            (6U)
#endif

/**
 * Version Release Month Day Value.
 */
#if !defined(IPSDK_VER_DATE_MDAY)
#define IPSDK_VER_DATE_MDAY           (15U)
#endif

/**
 * Version Release Date Text.
 */
#if !defined(IPSDK_VER_DATE_STR)
#define IPSDK_VER_DATE_STR()           "2023.06.15"
#endif


#endif /* IPSDK_VERSION_H_ */

